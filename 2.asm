ORG 00H
  MOV DPTR,#0300H
START:
  MOV R3,#00H
LOOP:
  MOV A,R3
  MOVC A,@A+DPTR
  CJNE A,#11111111B,OUT
  SJMP START
OUT:
  MOV P1,A
  CALL DELAY
  INC R3
  SJMP LOOP
DELAY:
  MOV R5,#100
DELAY2:
  MOV R6,#200
DELAY1:
  MOV R7,#250
  DJNZ R7,$
  DJNZ R6,DELAY1
  DJNZ R5,DELAY2
  RET
  ORG 300H
  DB 00000011B, 00000011B, 0001100B, 0001100B
  DB 00110000B, 00110000B, 11000000B, 11000000B,
  DB 11111111B
END
