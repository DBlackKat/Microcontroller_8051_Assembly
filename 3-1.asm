	ORG 00H
	JMP START
	ORG 50H
START:
	MOV R0,#0
	MOV R1,#010H
	MOV R3,#0
	MOV R4,#0
	MOV DPTR,#TABLE2
LOOP:
	MOV P1,#00H
	MOV A,R0
	MOVC A,@A+DPTR
	MOV P0,A
	MOV P1,R1
	INC R0
	MOV A,R1
	RR A
	MOV R1,A
	CALL DELAY
	CJNE R0,#5,LOOP
	MOV R0,#0
	MOV R1,#10H
	MOV DPTR,#TABLE2
	JMP START

TABLE2:
	DB	10111111B
	DB	11111011B
	DB	10100011B
	DB	11111011B
	DB	10111111B

DELAY:
	MOV R6,#20
DELAY1:
	MOV R7,#25
DELAY2:
	DJNZ R7,DELAY2
	DJNZ R6,DELAY1
	RET
END
